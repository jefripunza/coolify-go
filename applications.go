// Delete deletes a specific application.
// If stopAndRemove is true, the query param `delete_configurations=true` is sent (standard Coolify teardown behaviour).
func (s *ApplicationsService) Delete(ctx context.Context, uuid string, stopAndRemove bool) error {
	path := fmt.Sprintf("applications/%s", uuid)
	if stopAndRemove {
		path = fmt.Sprintf("%s?delete_configurations=true", path)
	}

	req, err := s.client.newRequest(http.MethodDelete, path, nil)
	if err != nil {
		return err
	}

	_, err = s.client.do(ctx, req, nil)
	return err
}

// LogsOptions specifies options when fetching application logs.
type LogsOptions struct {
	Lines int `url:"lines,omitempty"`
}

// GetLogs retrieves live or cached logs for the application container.
func (s *ApplicationsService) GetLogs(ctx context.Context, uuid string, opts ...LogsOptions) (string, error) {
	path := fmt.Sprintf("applications/%s/logs", uuid)
	if len(opts) > 0 && opts[0].Lines > 0 {
		path = fmt.Sprintf("%s?lines=%d", path, opts[0].Lines)
	}

	req, err := s.client.newRequest(http.MethodGet, path, nil)
	if err != nil {
		return "", err
	}

	var logs string
	_, err = s.client.do(ctx, req, &logs)
	return logs, err
}

