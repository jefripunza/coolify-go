package coolify

import (
	"context"
	"fmt"
	"net/http"
	"net/url"
)

// ApplicationsService handles communication with the application-related endpoints of the Coolify API.
type ApplicationsService struct {
	client *Client
}

// ListApplicationsOptions specifies optional parameters for listing applications.
type ListApplicationsOptions struct {
	Tag string `url:"tag,omitempty"`
}

// List retrieves all applications.
func (s *ApplicationsService) List(ctx context.Context, opts ...ListApplicationsOptions) ([]Application, error) {
	path := "applications"
	if len(opts) > 0 && opts[0].Tag != "" {
		path = fmt.Sprintf("applications?tag=%s", url.QueryEscape(opts[0].Tag))
	}

	req, err := s.client.newRequest(http.MethodGet, path, nil)
	if err != nil {
		return nil, err
	}

	var apps []Application
	_, err = s.client.do(ctx, req, &apps)
	return apps, err
}

// Get retrieves a specific application by its UUID.
func (s *ApplicationsService) Get(ctx context.Context, uuid string) (*Application, error) {
	path := fmt.Sprintf("applications/%s", uuid)
	req, err := s.client.newRequest(http.MethodGet, path, nil)
	if err != nil {
		return nil, err
	}

	app := new(Application)
	_, err = s.client.do(ctx, req, app)
	return app, err
}

